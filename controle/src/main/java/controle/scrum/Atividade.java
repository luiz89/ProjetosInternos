package controle.scrum;

public class Atividade {

	private Long codigo;
	private Long codigoTipoAtividade;
	private Long codigoSpring;
	private Long codigoStatus;

	public Long getCodigo() {
		return codigo;
	}

	public void setCodigo(Long codigo) {
		this.codigo = codigo;
	}

	public Long getCodigoTipoAtividade() {
		return codigoTipoAtividade;
	}

	public void setCodigoTipoAtividade(Long codigoTipoAtividade) {
		this.codigoTipoAtividade = codigoTipoAtividade;
	}

	public Long getCodigoSpring() {
		return codigoSpring;
	}

	public void setCodigoSpring(Long codigoSpring) {
		this.codigoSpring = codigoSpring;
	}

	public Long getCodigoStatus() {
		return codigoStatus;
	}

	public void setCodigoStatus(Long codigoStatus) {
		this.codigoStatus = codigoStatus;
	}

}
